// Print Multiplication table of a number
public class pro2 {
    public static void main(String[] args) {
        int number = 5; // You can change this number to print the table of a different number
        for (int i = 1; i <= 10; i++) {
            System.out.println(number + " x " + i + " = " + (number * i));
        }
    }
}