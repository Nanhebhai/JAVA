// declare all primitive types of variables and print them
public class pro1 {
        public static void main(String[] args) {
        // Declare variables of all primitive types
        short shortv = 1000;
        int intv = 100000;
        long longv = 100000L;
        float floatv = 10.5f;
        double doublev = 20.99;
        char charv = 'A';
        boolean booleanv = true;

        // variablee printing
        System.out.println("Short variable: " + shortv);
        System.out.println("Integer variable: " + intv);
        System.out.println("Long variable: " + longv);
        System.out.println("Float variable: " + floatv);
        System.out.println("Double variable: " + doublev);
        System.out.println("Character variable: " + charv);
        System.out.println("Boolean variable: " + booleanv);
    }
}
