// write a program to add two floating point numbers without taking any input from user
public class pro8 {
    public static void main(String[] args) 
    {
        float a = 5.5f;
        float b = 4.5f;

        float sum = a + b;

        System.out.println("The sum of " + a + " and " + b + " is: " + sum);
    }
}